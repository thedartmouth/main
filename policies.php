					<h1>Editorial Policy</h1>
						<p>The Dartmouth welcomes all contributions, which can be delivered to the Editor by paper mail to The Dartmouth, 6175 Robinson Hall, Hanover, NH 03755, in person at 221 Robinson Hall or via electronic mail to <a href="mailto:editor@thedartmouth.com">editor@thedartmouth.com</a>.</p>
					<p>	All submissions become property of The Dartmouth. Because The Dartmouth receives more letters and columns than available space can accommodate, not all submissions can be acknowledged or printed. Letters should not be longer than 250 words and must include the name, location and contact information of the author. Columns should be 700 words long, and observe formal rules of spelling and grammar. The Dartmouth reserves the right to edit all material.</p>
					<p>	An editorial that appears without a byline during Fall, Winter or Spring term represents the views of The Dartmouth. An editorial that appears without a byline during the summer reflects the views of the Summer term directorate. Any other column, letter or cartoon is the work of the attributed author and does not necessarily represent the views or opinions of The Dartmouth.</p>
