					<h1>Comments Policy</h1>
						<p>The Dartmouth welcomes comments from readers on <a href="/">TheDartmouth.com</a>. You can find answers to frequently asked questions about commenting below.</p>
						<h3>How do I submit a comment?</h3>
						<p>Comments may be submitted using the Disqus system below each article. </p>
						<h3>What kind of comments should I post?</h3>
						<p>The Dartmouth is looking for comments that further discussion on a given article or subject, or that provide constructive criticism. The Dartmouth will not allow comments that are off-topic or that contain vulgarity, inappropriate language or ad hominem attacks. The Dartmouth reserves the right to remove any comment at any time.</p>
						<h3>Do you edit comments?</h3>
						<p>Comments are not edited. They are either approved or deleted.</p>
						<h3>Is commenting allowed on all articles?</h3>
						<p>The Dartmouth reserves the right to disable commenting on any article.</p>
						<h3>Should I use my real name when making a comment?</h3>
						<p>We encourage all readers to use their real names when commenting. This gives credibility to comments and provides for a more substantive, honest discussion.</p>
						<h3>Should I post new information about a breaking news story? What if I see an error in a blog post or article? What is the best way to suggest a correction?</h3>
						<p>The best way to contact The Dartmouth about any of these issues is to e-mail <a href="mailto:editor@thedartmouth.com">editor@thedartmouth.com</a>.</p>
