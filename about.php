						<h1>About Us</h1>
						<p>The Dartmouth, founded in 1799, is the student newspaper of Dartmouth College and the campus’s only daily. The Dartmouth is published by The Dartmouth, Inc., an independent, nonprofit corporation chartered in the state of New Hampshire.</p>
						<p>The Dartmouth is published every day, Monday through Friday, from September to June, except on federal holidays and College vacations. In addition to the regular newspaper, The Dartmouth publishes the Sports Weekly on Mondays and the Mirror, The Dartmouth’s weekly magazine, on Fridays. Over the summer, the paper is published on Tuesdays and Fridays.</p>
						<p>During the fall, winter and spring terms, The Dartmouth's editorial board publishes a house editorial, the Verbum Ultimum, in the Friday edition of the paper. The editorial board is composed of the editor-in-chief, publisher, executive editors and opinion editors.</p>

